    private void categorySubmitBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_categorySubmitBtnActionPerformed
        if("".equals(categoryName.getText()) )
        {
            JOptionPane.showMessageDialog(this, "Category Name can't be empty", "Error", JOptionPane.WARNING_MESSAGE);
        }else{
            String SQL = "INSERT INTO category (name, description)"
                        + "VALUES ('"+ categoryName.getText()+"', '"+categoryDescription.getText()+"')";
            int status = Database.execute(SQL);
            if(status == 1)
            {
                JOptionPane.showMessageDialog(this, "Added Category Success", "Sukses", JOptionPane.INFORMATION_MESSAGE);
                selectData();
            }else{
                JOptionPane.showMessageDialog(this, "Failed Add Category", "Error", JOptionPane.WARNING_MESSAGE);
            }
        }
    }//GEN-LAST:event_categorySubmitBtnActionPerformed

    private void categoryDeleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_categoryDeleteBtnActionPerformed
       int line = categoryTable.getSelectedRow();
       if(line != -1){
           String ID    = categoryTable.getValueAt(line, 0).toString();
           String SQL   = "DELETE FROM t_siswa WHERE id = '"+ID+"'";
           int status   = Database.execute(SQL);
           
           if(status == 1){
               JOptionPane.showMessageDialog(this, "Deleted category success", "Success", JOptionPane.INFORMATION_MESSAGE);
               selectData();
           }else{
               JOptionPane.showMessageDialog(this, "Upps !! wrong delete category", "Error", JOptionPane.WARNING_MESSAGE);
           }
       }else{
           JOptionPane.showMessageDialog(this, "Chose the line first !!", "Error", JOptionPane.WARNING_MESSAGE);
       }
    }//GEN-LAST:event_categoryDeleteBtnActionPerformed

