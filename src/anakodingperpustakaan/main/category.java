    private void categoryEditBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_categoryEditBtnActionPerformed
        
        int line = categoryTable.getSelectedRow();
        
         if(line != -1){
           String ID    = categoryTable.getValueAt(line, 0).toString();
           
           if("".equals(categoryName.getText()) )
        {
            JOptionPane.showMessageDialog(this, "Category Name can't be empty", "Error", JOptionPane.WARNING_MESSAGE);
        }else{
            
            String SQL = "UPDATE category SET "
                        + "name = '"+categoryName.getText()+"', "
                        + "description ='"+categoryDescription.getText()+"'"
                        + "WHERE id = '"+ID+"'";
            
            int status = Database.execute(SQL);
            if(status == 1)
            {
                JOptionPane.showMessageDialog(this, "Updated category success", "Success", JOptionPane.INFORMATION_MESSAGE);
                selectData();
            }else{
                JOptionPane.showMessageDialog(this, "Updated category failed", "Error", JOptionPane.WARNING_MESSAGE);
            }
        }
           
       }else{
           JOptionPane.showMessageDialog(this, "Chose the line first !!", "Error", JOptionPane.WARNING_MESSAGE);
       }
        
        
        
        
    }//GEN-LAST:event_categoryEditBtnActionPerformed

